var n1="10";
var n2="20";

//n1 and n2 value before swap
console.log("before swap");
console.log("n1->",n1);
console.log("n2->",n2);

//third variable declaration
var tempt=n1;

//swaping the value
n1=n2;
n2=tempt;

//n1 and n2 value after swap
console.log("After swap");
console.log("n1->",n1);
console.log("n2->",n2);